// ─── Helpers ─────────────────────────────────────────────────────────────────

function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

// ─── Parse contest output file ───────────────────────────────────────────────
// This was generated by an earlier analysis step and has properly-parsed
// multi-line contest headers, candidates, and yes/no detection.

function parseContestOutput(filePath) {
  const text = fs.readFileSync(filePath, 'utf-8');
  const contests = [];

  // Split into PARTISAN and JUDICIAL sections
  const sections = text.split(
    /^={5,}$/m
  );

  for (const section of sections) {
    const blockRegex =
      /--- Contest Block #(\d+) ---\nFull Header: (.+)\n([\s\S]*?)(?=--- Contest Block|Total unique|$)/g;
    let match;
    while ((match = blockRegex.exec(section)) !== null) {
      const header = match[2].trim();
      const body = match[3];

      // Skip continuation blocks (empty body, just a repeated sub-header)
      const hasCandidates = /^\s+-\s+/m.test(body);
      if (!hasCandidates) continue;

      // Determine if it's a multi-contest block
      const individualMatch = body.match(
        /Individual Contests:\n([\s\S]*?)(?=\s+Candidates)/
      );

      let contestNames = [];
      if (individualMatch) {
        const icRegex = /^\s+-\s+\d+\s+(.+)$/gm;
        let m;
        while ((m = icRegex.exec(individualMatch[1])) !== null) {
          contestNames.push(m[1].trim());
        }
      }

      if (contestNames.length === 0) {
        // Single contest - strip leading "N " from header
        const cleanName = header.replace(/^\d+\s+/, '');
        contestNames = [cleanName];
      }

      // Extract candidates
      const candidates = [];
      const candRegex =
        /^\s+-\s+(DEM|REP|LIB|UST|GRN|WCP|NLP|NPA|NP)\s+(.+)$/gm;
      let cm;
      while ((cm = candRegex.exec(body)) !== null) {
        candidates.push({
          party: cm[1] === 'NP' ? null : cm[1],
          name: cm[2].trim().replace(/\s*-\s*$/, ''),
        });
      }

      // Check for Yes/No options
      const yesNoMatch = body.match(/^\s+-\s+(Yes|No)$/m);
      const isYesNo = !!yesNoMatch;
      if (isYesNo) {
        // Count yes/no pairs (for multi-proposal blocks)
        const yesCount = (body.match(/^\s+-\s+Yes$/gm) || []).length;
      }

      // For multi-contest blocks, split candidates between contests
      if (contestNames.length > 1) {
        // Divide candidates roughly equally between contests
        const perContest = Math.ceil(
          candidates.length / contestNames.length
        );
        for (let i = 0; i < contestNames.length; i++) {
          const start = i * perContest;
          const end = Math.min(start + perContest, candidates.length);
          contests.push({
            name: contestNames[i],
            candidates: candidates.slice(start, end),
            isYesNo,
          });
        }
      } else {
        contests.push({
          name: contestNames[0],
          candidates,
          isYesNo,
        });
      }
    }
  }
  return contests;
}

