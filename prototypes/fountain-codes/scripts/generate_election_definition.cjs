// Additional districts for congressional, state leg, county commissioner
const additionalDistricts = new Map();
const allDistrictIds = new Set(districts.map((d) => d.id));
function getOrCreateDistrict(id, name) {
  if (!allDistrictIds.has(id)) {
    allDistrictIds.add(id);
    additionalDistricts.set(id, { id, name });
    districts.push({ id, name });
  }
  return id;
}

// Assign each contest to a district
const contestDistrictAssignments = new Map(); // contestName -> districtId
// Also track which districts need to be added to which precincts
const extraPrecinctDistricts = new Map(); // precinctName -> Set<districtId>

function addDistrictToJurisdiction(jur, districtId) {
  const precincts = jurisdictions.get(jur);
  if (!precincts) return;
  for (const p of precincts) {
    if (!extraPrecinctDistricts.has(p)) extraPrecinctDistricts.set(p, new Set());
    extraPrecinctDistricts.get(p).add(districtId);
  }
}

// Skip MACOMB contests entirely
const SKIP = (name) => name.includes('MACOMB');

for (const [contestName] of contestMap) {
  if (SKIP(contestName)) continue;

  // Check county-wide
  if (COUNTY_WIDE_PATTERNS.some((p) => p.test(contestName))) {
    contestDistrictAssignments.set(contestName, COUNTY_WIDE_DISTRICT);
    continue;
  }

  // Check district-specific
  let matched = false;
  for (const dp of DISTRICT_PATTERNS) {
    const m = contestName.match(dp.regex);
    if (m) {
      const dId = getOrCreateDistrict(dp.makeId(m), dp.makeName(m));
      contestDistrictAssignments.set(contestName, dId);
      matched = true;
      break;
    }
  }
  if (matched) continue;

  // Check jurisdiction-specific
  const jur = matchJurisdiction(contestName);
  if (jur) {
    if (typeof jur === 'string') {
      const dId = jurisdictionDistrictMap.get(jur);
      if (dId) {
        contestDistrictAssignments.set(contestName, dId);
        continue;
      }
    } else if (Array.isArray(jur)) {
      if (jur.length === 1) {
        // Single jurisdiction in array form
        const dId = jurisdictionDistrictMap.get(jur[0]);
        if (dId) {
          contestDistrictAssignments.set(contestName, dId);
          continue;
        }
      }
      // Multi-jurisdiction: create a combined district
      // Use contest name as district name to ensure uniqueness
      const combinedId = slugify(contestName).substring(0, 60);
      const combinedName = contestName;
      getOrCreateDistrict(combinedId, combinedName);
      contestDistrictAssignments.set(contestName, combinedId);
      // Add this district to all precincts in these jurisdictions
      for (const j of jur) {
        addDistrictToJurisdiction(j, combinedId);
      }
      continue;
    }
  }

  // Wayne County Community College → county-wide
  if (contestName.includes('Wayne County Community College')) {
    contestDistrictAssignments.set(contestName, COUNTY_WIDE_DISTRICT);
    continue;
  }

  // Fallback: try to find jurisdiction from contest name keywords
  let found = false;
  for (const [jurName, jurId] of jurisdictionDistrictMap) {
    if (
      contestName.includes(jurName) ||
      (jurName.startsWith('City of ') &&
        contestName.includes(jurName.replace('City of ', '')))
    ) {
      contestDistrictAssignments.set(contestName, jurId);
      found = true;
      break;
    }
  }
  if (found) continue;

  // Last resort: put on county-wide
  console.log(`  Warning: no district match for: ${contestName}`);
  contestDistrictAssignments.set(contestName, COUNTY_WIDE_DISTRICT);
}

